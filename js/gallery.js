	function shuffleImgs(array) {
	    for (var i = array.length - 1; i > 0; i--) {
	        var j = Math.floor(Math.random() * (i + 1));
	        var temp = array[i];
	        array[i] = array[j];
	        array[j] = temp;
	    }
	    return array;
	}


	function createCallback(i){
		return function(){
			vex.dialog.alert({
				message: imagesObj[i].name,
				className: 'vex-theme-wireframe',
				image: imagesObj[i].imageurl
			});
		}
	}

	$(document).ready(function() {
		var imagesObjShuf = shuffleImgs(imagesObj);
		for(var i = 0; i <= imagesObjShuf.length-1; i++) {
		 $('#portfolio').append('<div class="thumbnail all image' + i + ' ' + imagesObjShuf[i].categories[0] + ' ' + imagesObjShuf[i].categories[1] + '"><img src="' + imagesObjShuf[i].imageurl + '" class="' + imagesObjShuf[i].orientation + '"></div>' );
		 $('.image' + i).click( createCallback(i) );
		}
	});
